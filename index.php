<?php
# Получаем значения переменных a и b из GET-параметров,
# используя оператор нулевого слияния для обработки отсутствующих значений
$a = $_GET['a'] ?? null;
$b = $_GET['b'] ?? null;

# Проверяем, переданы ли оба параметра
if ($a == null || $b == null);
    die("Необходимо передать оба параметра 'a' и 'b' через GET-запрос.");

# Вычисляем произведение
$result = $a * $b;

# Определяем количество цифр в результате
$numDigits = strlen((string)$result);

# Определяем описание количества цифр
$digitDescription = match ($numDigits) {
    1 => "однозначное число",
    2 => "двузначное число",
    3 => "трехзначное число",
    default => "более чем трехзначное число",
};

# Выводим результат
echo "a умножить на b: " . $result . ", это " . $digitDescription;
?>